<h2>Transreal</h2>

<p>The TRANSREAL project is a proposal for a Flagship Research Pilot, at a 
European level, to catalyze a major qualitative advance in the relationship 
between human beings and the world's computing infrastructure, which we call 
<a href="real_world_computing.php">real-world computing</a>. By real-world 
computing we mean that the world's computing infrastructure will process 
complex real-world data to interact with human beings at a human level of 
sensory input/output and intellectual concepts. Real-world computing will 
provide full media interchangeability (real-time translation of human 
languages between textual, audio, and video forms). Real-world computing will 
extract knowledge from the vast body of raw digital content (text, audio, 
video) and use it to build creative solutions to problems specified by humans. 
Real-world computing will provide expert guidance in real time to human 
activities that require specialized knowledge.</p>

<h2>Discussion</h2>

<p> We want to discuss the vision of TRANSREAL and the important role that 
free and open source software plays on this vision, specially the community 
around data analysis. TRANSREAL aims to bring together two key disciplines, 
machine learning and distributed computing, and all relevant application 
disciplines. Machine learning can solve highly complex problems, which are 
infeasible to program explicitly. Distributed computing can provide enormous 
resources orders of magnitude beyond what is currently available, by using the 
concept of elasticity. We aim to create durable collaborative communities 
combining all these disciplines, and to build applications of <a 
href="real_world_computing.php">real-world computing</a>. We believe that free 
and open source communities can largely benefit from TRANSREAL's results, as 
well as the benefit that TRANSREAL can obtain from free and open source 
solutions, because they are a key element to build communities. We expect to 
motivate the FOSS community to become part of the Flagship workprogramme, 
which will give a chance for the FOSS community to directly influence EU 
research. </p>

<h2> Current FLOSS activities </h2>

<p> Two partners of TRANSREAL are strongly related to free software: 
Initiative de Recherche et Innovation sur le Logiciel Libre (IRILL) and the 
Free Cloud Alliance (FCA). Also, two of the academic partners has important 
releases related to the topic. The Zuse Institute Berlin (ZIB) maintains <a 
