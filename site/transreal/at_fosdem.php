transactional database. The Universit&eacute; catholique de Louvain (UCL) 
maintains <a href="http://beernet.info.ucl.ac.be"> Beernet </a>, a 
peer-to-peer system with transactional storage, and the <a 
href="http://www.mozart-oz.org">Mozart Programming Language</a>.</p>

